#include "../includes/minishell.h"

int		is_space(char c)
{
	return (c == ' ' || c == '\t');
}

int		is_operands(char c)
{
	if (c == '>' || c == '<' || c == '&' || c == '|' ||
		c == '(' || c == ')' || c == ';')
		return (1);
	else
		return (0);
}
