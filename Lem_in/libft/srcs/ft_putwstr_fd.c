#include "libft.h"

void	ft_putwstr_fd(wchar_t const *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		ft_putwchar_fd((wchar_t)(s[i]), fd);
		i++;
	}
}
