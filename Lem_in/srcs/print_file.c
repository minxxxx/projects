#include "lem_in.h"

void	print_file(t_env *env)
{
	int		i;

	if (!env->file)
		return ;
	i = 0;
	while (env->file[i])
	{
		ft_putstr("\033[0;32m");
		ft_putendl(env->file[i]);
		i++;
		ft_putstr("\033[1;37m\033[0;37m");
	}
}
