#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	int		len;
	char	*temp;

	i = 0;
	len = (int)n;
	temp = (char*)s;
	if (len)
	{
		while (i < len)
		{
			temp[i] = '\0';
			i++;
		}
	}
}
