#include <rt.h>

t_vector	vector_norm(t_vector *v)
{
	double	m;

	m = sqrt(SQR(v->x) + SQR(v->y) + SQR(v->z));
	if (m != 0)
	{
		v->x /= m;
		v->y /= m;
		v->z /= m;
	}
	return (*v);
}
