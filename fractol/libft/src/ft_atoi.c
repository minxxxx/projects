#include "../includes/libft.h"

int		ft_atoi(char *s)
{
	int		i;
	int		neg;
	int		res;

	i = 0;
	neg = 1;
	res = 0;
	while (s[i] == ' ' || s[i] == '\t')
		i++;
	if (s[i] == '-')
	{
		neg = -neg;
	}
	while (s[i] != '\0')
	{
		if (s[i] >= '0' && s[i] <= '9')
			res = res * 10 + (s[i] - '0');
		i++;
	}
	return (res * neg);
}
