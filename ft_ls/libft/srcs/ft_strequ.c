int	ft_strequ(char const *s1, char const *s2)
{
	unsigned int	i;

	i = 0;
	while (s1[i])
	{
		if (!s2[i])
			return (0);
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	if (s2[i])
		return (0);
	return (1);
}
