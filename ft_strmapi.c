#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int					len;
	unsigned int		i;
	char				*str;

	len = ft_strlen((char*)s);
	str = (char *)malloc(sizeof(char*) * (len + 1));
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	return (str);
}
