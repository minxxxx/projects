#include "libft.h"

void	ft_putul_fd(size_t n, int fd)
{
	if (n > 9)
	{
		ft_putul_fd(n / 10, fd);
		ft_putul_fd(n % 10, fd);
	}
	else
		ft_putchar_fd('0' + n, fd);
}
